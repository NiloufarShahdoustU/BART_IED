function effectTable = createExpectedRewardEffectTable( ...
    beta, robustCovariance, predictorNames, observedExpectedReward)

    beta = beta(:);
    observedExpectedReward = double(observedExpectedReward(:));
    observedExpectedReward = observedExpectedReward( ...
        isfinite(observedExpectedReward));

    if isempty(observedExpectedReward)
        error('No finite expected-reward values were available.');
    end

    minimumExpectedReward = min(observedExpectedReward);
    maximumExpectedReward = max(observedExpectedReward);
    referenceExpectedReward = mean(observedExpectedReward, 'omitnan');

    if maximumExpectedReward == minimumExpectedReward
        expectedRewardGrid = minimumExpectedReward;
    else
        expectedRewardGrid = linspace( ...
            minimumExpectedReward, maximumExpectedReward, 120)';
    end

    IEDIndex = find( ...
        predictorNames == "active_ied_count", 1);
    expectedRewardIndex = find( ...
        predictorNames == "expected_reward", 1);
    interactionIndex = find( ...
        predictorNames == "active_ied_count_x_expected_reward", 1);

    if isempty(IEDIndex) || isempty(expectedRewardIndex) || ...
            isempty(interactionIndex)
        error('Required expected-reward predictors were not found.');
    end

    nGrid = length(expectedRewardGrid);
    logRelativeHazardNoIED = zeros(nGrid, 1);
    logRelativeHazardPostIED = zeros(nGrid, 1);
    seNoIED = zeros(nGrid, 1);
    sePostIED = zeros(nGrid, 1);

    for gg = 1:nGrid

        currentExpectedReward = expectedRewardGrid(gg);

        % Reference is: zero active IEDs at mean expected reward.
        noIEDContrast = zeros(length(beta), 1);
        noIEDContrast(expectedRewardIndex) = ...
            currentExpectedReward - referenceExpectedReward;

        postIEDContrast = zeros(length(beta), 1);
        postIEDContrast(IEDIndex) = 1;
        postIEDContrast(expectedRewardIndex) = ...
            currentExpectedReward - referenceExpectedReward;
        postIEDContrast(interactionIndex) = currentExpectedReward;

        logRelativeHazardNoIED(gg) = noIEDContrast' * beta;
        logRelativeHazardPostIED(gg) = postIEDContrast' * beta;

        varianceNoIED = noIEDContrast' * ...
            robustCovariance * noIEDContrast;
        variancePostIED = postIEDContrast' * ...
            robustCovariance * postIEDContrast;

        seNoIED(gg) = sqrt(max(varianceNoIED, 0));
        sePostIED(gg) = sqrt(max(variancePostIED, 0));

    end

    relativeHazardNoIED = exp(logRelativeHazardNoIED);
    relativeHazardNoIEDCILow = exp( ...
        logRelativeHazardNoIED - 1.96 .* seNoIED);
    relativeHazardNoIEDCIHigh = exp( ...
        logRelativeHazardNoIED + 1.96 .* seNoIED);

    relativeHazardPostIED = exp(logRelativeHazardPostIED);
    relativeHazardPostIEDCILow = exp( ...
        logRelativeHazardPostIED - 1.96 .* sePostIED);
    relativeHazardPostIEDCIHigh = exp( ...
        logRelativeHazardPostIED + 1.96 .* sePostIED);

    effectTable = table( ...
        expectedRewardGrid, ...
        relativeHazardNoIED, ...
        relativeHazardNoIEDCILow, ...
        relativeHazardNoIEDCIHigh, ...
        relativeHazardPostIED, ...
        relativeHazardPostIEDCILow, ...
        relativeHazardPostIEDCIHigh, ...
        repmat(referenceExpectedReward, nGrid, 1), ...
        'VariableNames', { ...
            'expectedReward', ...
            'relativeHazardNoIED', ...
            'relativeHazardNoIEDCILow', ...
            'relativeHazardNoIEDCIHigh', ...
            'relativeHazardPostIED', ...
            'relativeHazardPostIEDCILow', ...
            'relativeHazardPostIEDCIHigh', ...
            'referenceExpectedReward'});

end

