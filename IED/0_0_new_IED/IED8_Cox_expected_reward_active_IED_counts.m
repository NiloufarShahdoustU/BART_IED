function result = runOverallMechanisticCoxAnalysis( ...
    inputFolderName_LFPIED, ...
    inputFolderName_modeling, ...
    config, ...
    settings)

    codeChar = char(config.code);
    postIEDWindowMilliseconds = config.postIEDWindowMilliseconds;
    postIEDWindowSeconds = postIEDWindowMilliseconds / 1000;

    fileList = dir(fullfile( ...
        inputFolderName_LFPIED, '*.LFPIED.mat'));

    modelFileList = dir(fullfile( ...
        inputFolderName_modeling, '*TDdataParamRecovery.mat'));

    if isempty(fileList)
        error('%s: no .LFPIED.mat files were found.', codeChar);
    end

    if isempty(modelFileList)
        error('%s: no *TDdataParamRecovery.mat files were found.', ...
            codeChar);
    end

    countingProcessData = emptyMechanisticCountingProcessTable();
    trialSummary = emptyMechanisticTrialSummaryTable();

    nSkippedMissingModelFile = 0;
    nSkippedInvalidModelFile = 0;
    nLoadedParticipants = 0;
    totalIEDsRetained = 0;

    %% Build overall counting-process data

    for pt = 1:length(fileList)

        fileName = fileList(pt).name;
        fileNameParts = strsplit(fileName, '.');
        patientID = string(fileNameParts{1});

        fprintf('\n%s: processing patient %s\n', ...
            codeChar, char(patientID));

        loadedData = load(fullfile( ...
            inputFolderName_LFPIED, fileName));

        if ~isfield(loadedData, 'LFPIED')
            fprintf('%s: skipped; LFPIED structure was not found.\n', ...
                codeChar);
            continue;
        end

        modelFilePath = findModelFileForPatient( ...
            inputFolderName_modeling, modelFileList, patientID);

        if strlength(modelFilePath) == 0
            fprintf(['%s: skipped; no unique expected-reward model file ' ...
                'was found for %s.\n'], codeChar, char(patientID));
            nSkippedMissingModelFile = nSkippedMissingModelFile + 1;
            continue;
        end

        modelData = load(char(modelFilePath));

        [expectedRewardsRaw, expectedRewardReadStatus] = ...
            readExpectedReward(modelData);

        if expectedRewardReadStatus ~= "ok"
            fprintf('%s: skipped; %s\n', ...
                codeChar, char(expectedRewardReadStatus));
            nSkippedInvalidModelFile = nSkippedInvalidModelFile + 1;
            continue;
        end

        LFPIED = loadedData.LFPIED;

        requiredFields = { ...
            'RTs', ...
            'isControl', ...
            'balloonType', ...
            char(config.durationField), ...
            char(config.iedField)};

        if config.eventMode == "bankedOnly"
            requiredFields{end + 1} = 'BankedTrials';
        end

        requiredFields = unique(requiredFields, 'stable');
        missingField = false;

        for ff = 1:length(requiredFields)
            if ~isfield(LFPIED, requiredFields{ff})
                fprintf('%s: skipped; missing field %s.\n', ...
                    codeChar, requiredFields{ff});
                missingField = true;
            end
        end

        if missingField
            continue;
        end

        RTs = double(LFPIED.RTs(:));
        durationValues = LFPIED.(char(config.durationField));
        durationValues = double(durationValues(:));
        isControl = double(LFPIED.isControl(:));
        balloonType = double(LFPIED.balloonType(:));

        if config.eventMode == "bankedOnly"
            bankedTrials = double(LFPIED.BankedTrials(:));
        else
            bankedTrials = NaN(size(durationValues));
        end

        vectorLengths = [ ...
            length(RTs), ...
            length(durationValues), ...
            length(isControl), ...
            length(balloonType), ...
            length(expectedRewardsRaw)];

        if config.eventMode == "bankedOnly"
            vectorLengths(end + 1) = length(bankedTrials);
        end

        if isfield(LFPIED, 'nTrials') && ...
                isscalar(LFPIED.nTrials) && ...
                isfinite(LFPIED.nTrials)
            vectorLengths(end + 1) = double(LFPIED.nTrials);
        end

        nTrials = floor(min(vectorLengths));

        if nTrials < 1
            fprintf('%s: skipped; no aligned trials.\n', codeChar);
            continue;
        end

        RTs = RTs(1:nTrials);
        durationValues = durationValues(1:nTrials);
        isControl = isControl(1:nTrials);
        balloonType = balloonType(1:nTrials);
        bankedTrials = bankedTrials(1:nTrials);
        expectedRewards = double(expectedRewardsRaw(1:nTrials));

        balloonColorCode = mapBalloonColorCode(balloonType);
        samplingFrequencyHz = getSamplingFrequency( ...
            LFPIED, settings.defaultSamplingFrequencyHz);

        IEDoccurrence = LFPIED.(char(config.iedField));
        participantHadValidTrial = false;

        for trial = 1:nTrials

            durationSeconds = durationValues(trial);

            if config.eventMode == "bankedOnly"
                finalEventObserved = bankedTrials(trial) == 1;
            else
                finalEventObserved = true;
            end

            keepTrial = ...
                isfinite(RTs(trial)) && ...
                RTs(trial) > 0 && ...
                RTs(trial) <= settings.maximumRTSeconds && ...
                isfinite(durationSeconds) && ...
                durationSeconds > 0 && ...
                isfinite(balloonColorCode(trial)) && ...
                ismember(balloonColorCode(trial), [1 2 3]) && ...
                isfinite(expectedRewards(trial));

            if settings.useOnlyNonControlTrials
                keepTrial = keepTrial && isControl(trial) == 0;
            end

            if config.eventMode == "bankedOnly"
                keepTrial = keepTrial && ...
                    isfinite(bankedTrials(trial)) && ...
                    ismember(bankedTrials(trial), [0 1]);
            end

            if ~keepTrial
                continue;
            end

            participantHadValidTrial = true;

            trialRows = makeMechanisticPostIEDRows( ...
                patientID, ...
                trial, ...
                durationSeconds, ...
                IEDoccurrence, ...
                samplingFrequencyHz, ...
                balloonColorCode(trial), ...
                expectedRewards(trial), ...
                finalEventObserved, ...
                postIEDWindowSeconds);

            countingProcessData = [ ...
                countingProcessData; trialRows]; 

            nIEDsInTrial = countIEDsInTrial( ...
                IEDoccurrence, trial, durationSeconds, ...
                samplingFrequencyHz);

            totalIEDsRetained = totalIEDsRetained + nIEDsInTrial;

            postIEDExposureSeconds = sum( ...
                (trialRows.tStop - trialRows.tStart) .* ...
                trialRows.postIED, ...
                'omitnan');

            eventInPostIEDWindow = double( ...
                finalEventObserved && ...
                trialRows.postIED(end) == 1);

            if config.eventMode == "bankedOnly"
                bankedTrialValue = bankedTrials(trial);
            else
                bankedTrialValue = NaN;
            end

            newTrialSummaryRow = table( ...
                patientID, ...
                trial, ...
                durationSeconds, ...
                expectedRewards(trial), ...
                bankedTrialValue, ...
                double(finalEventObserved), ...
                eventInPostIEDWindow, ...
                nIEDsInTrial, ...
                postIEDExposureSeconds, ...
                balloonColorCode(trial), ...
                samplingFrequencyHz, ...
                'VariableNames', ...
                    trialSummary.Properties.VariableNames);

            trialSummary = [ ...
                trialSummary; newTrialSummaryRow]; 

        end

        if participantHadValidTrial
            nLoadedParticipants = nLoadedParticipants + 1;
            fprintf('%s: expected-reward file: %s\n', ...
                codeChar, char(modelFilePath));
        end

    end

    if isempty(countingProcessData)
        error('%s: no valid counting-process rows were created.', ...
            codeChar);
    end

    %% Build predictors

    [countingProcessData.patientStratum, patientLevels] = ...
        findgroups(countingProcessData.patientID);

    [X, predictorNames, referenceBalloonColor] = ...
        buildMechanisticPredictorMatrix(countingProcessData);

    T = [countingProcessData.tStart, countingProcessData.tStop];
    censoring = logical(countingProcessData.censored);
    strata = countingProcessData.patientStratum;

    validRows = ...
        all(isfinite(X), 2) & ...
        all(isfinite(T), 2) & ...
        T(:, 1) >= 0 & ...
        T(:, 2) > T(:, 1) & ...
        isfinite(strata);

    X = X(validRows, :);
    T = T(validRows, :);
    censoring = censoring(validRows);
    strata = strata(validRows);
    countingProcessData = countingProcessData(validRows, :);

    if size(X, 1) <= size(X, 2)
        error('%s: insufficient rows for the predictor matrix.', ...
            codeChar);
    end

    if length(unique(countingProcessData.patientID)) < 2
        error('%s: fewer than two participants remained.', codeChar);
    end

    if range(countingProcessData.activeIEDWindowCount) == 0
        error('%s: active IED count has no variation.', codeChar);
    end

    if range(countingProcessData.expectedReward) == 0
        error('%s: expected reward has no variation.', codeChar);
    end

    nEndpointEvents = sum(countingProcessData.eventAtStop == 1);

    nEventsOutsideWindow = sum( ...
        countingProcessData.eventAtStop == 1 & ...
        countingProcessData.postIED == 0);

    nEventsInsideWindow = sum( ...
        countingProcessData.eventAtStop == 1 & ...
        countingProcessData.postIED == 1);

    timeOutsideWindowSeconds = sum( ...
        (countingProcessData.tStop - countingProcessData.tStart) .* ...
        (countingProcessData.postIED == 0), ...
        'omitnan');

    timeInsideWindowSeconds = sum( ...
        (countingProcessData.tStop - countingProcessData.tStart) .* ...
        (countingProcessData.postIED == 1), ...
        'omitnan');

    if timeInsideWindowSeconds <= 0
        error('%s: no post-IED exposure time was available.', ...
            codeChar);
    end

    if nEventsInsideWindow < 1 || nEventsOutsideWindow < 1
        error(['%s: at least one endpoint event is required both inside ' ...
            'and outside post-IED windows.'], codeChar);
    end

    %% Fit participant-stratified Cox model

    coxOptions = statset('coxphfit');
    coxOptions.Display = 'final';
    coxOptions.MaxIter = 1000;
    coxOptions.MaxFunEvals = 5000;

    [beta, logLikelihood, baselineCumulativeHazard, stats] = ...
        coxphfit( ...
            X, ...
            T, ...
            'Censoring', censoring, ...
            'Strata', strata, ...
            'Ties', 'efron', ...
            'Baseline', 0, ...
            'Options', coxOptions);

    %% Participant-clustered robust inference

    [clusterRobustCovariance, clusterRobustSE, ...
        clusterRobustZ, clusterRobustP] = ...
        computeClusterRobustInference( ...
            stats, beta, countingProcessData.patientStratum);

    %% Results table

    betaCILow = beta - 1.96 .* clusterRobustSE;
    betaCIHigh = beta + 1.96 .* clusterRobustSE;

    hazardRatio = exp(beta);
    hazardRatioCILow = exp(betaCILow);
    hazardRatioCIHigh = exp(betaCIHigh);

    interpretation = createPredictorInterpretations( ...
        predictorNames, config);

    coxResults = table( ...
        predictorNames(:), ...
        beta(:), ...
        stats.se(:), ...
        stats.z(:), ...
        stats.p(:), ...
        clusterRobustSE(:), ...
        clusterRobustZ(:), ...
        clusterRobustP(:), ...
        betaCILow(:), ...
        betaCIHigh(:), ...
        hazardRatio(:), ...
        hazardRatioCILow(:), ...
        hazardRatioCIHigh(:), ...
        interpretation(:), ...
        'VariableNames', { ...
            'predictor', ...
            'beta_logHazard', ...
            'modelBasedSE', ...
            'modelBasedZ', ...
            'modelBasedPValue', ...
            'clusterRobustSE', ...
            'clusterRobustZ', ...
            'pValue', ...
            'betaCILow', ...
            'betaCIHigh', ...
            'hazardRatio', ...
            'hazardRatioCILow', ...
            'hazardRatioCIHigh', ...
            'interpretation'});

    %% Create adjusted expected-reward curves

    modelEffectTable = createExpectedRewardEffectTable( ...
        beta, ...
        clusterRobustCovariance, ...
        predictorNames, ...
        trialSummary.expectedReward);

    %% Print summary

    interactionIndex = find( ...
        predictorNames == "active_ied_count_x_expected_reward", 1);

    expectedRewardIndex = find( ...
        predictorNames == "expected_reward", 1);

    IEDIndex = find( ...
        predictorNames == "active_ied_count", 1);

    fprintf('\n============================================================\n');
    fprintf('%s OVERALL MECHANISTIC COX MODEL\n', codeChar);
    fprintf('============================================================\n');
    fprintf('post-IED window: %d ms\n', postIEDWindowMilliseconds);
    fprintf('participants: %d\n', length(patientLevels));
    fprintf('trials: %d\n', height(trialSummary));
    fprintf('counting-process rows: %d\n', ...
        height(countingProcessData));
    fprintf('all retained IED rows: %d\n', totalIEDsRetained);
    fprintf('endpoint events: %d\n', nEndpointEvents);
    fprintf('events outside post-IED window: %d\n', ...
        nEventsOutsideWindow);
    fprintf('events inside post-IED window: %d\n', ...
        nEventsInsideWindow);
    fprintf('at-risk time outside window: %.3f s\n', ...
        timeOutsideWindowSeconds);
    fprintf('at-risk time inside window: %.3f s\n', ...
        timeInsideWindowSeconds);
    fprintf('reference balloon color: %s\n', ...
        char(referenceBalloonColor));
    fprintf('log likelihood: %.6f\n', logLikelihood);
    fprintf('participants loaded: %d\n', nLoadedParticipants);
    fprintf('skipped for missing model file: %d\n', ...
        nSkippedMissingModelFile);
    fprintf('skipped for invalid model file: %d\n\n', ...
        nSkippedInvalidModelFile);

    disp(coxResults);

    fprintf('\n%s primary interaction result:\n', codeChar);
    fprintf('beta_IEDxExpectedReward = %.6f\n', ...
        beta(interactionIndex));
    fprintf('95%% beta CI = [%.6f, %.6f]\n', ...
        betaCILow(interactionIndex), ...
        betaCIHigh(interactionIndex));
    fprintf('interaction hazard ratio = %.6f\n', ...
        hazardRatio(interactionIndex));
    fprintf('95%% HR CI = [%.6f, %.6f]\n', ...
        hazardRatioCILow(interactionIndex), ...
        hazardRatioCIHigh(interactionIndex));
    fprintf('p = %.6g\n', clusterRobustP(interactionIndex));

    slopeOutsideIED = beta(expectedRewardIndex);
    slopeInsideIED = beta(expectedRewardIndex) + ...
        beta(interactionIndex);

    fprintf('expected-reward slope at 0 active IEDs = %.6f\n', ...
        slopeOutsideIED);
    fprintf('expected-reward slope at 1 active IED = %.6f\n', ...
        slopeInsideIED);
    fprintf('coefficient per additional active IED at expected reward = 0: %.6f\n', ...
        beta(IEDIndex));

    if clusterRobustP(interactionIndex) < 0.05
        if beta(interactionIndex) > 0
            fprintf(['conclusion: expected reward has a significantly ' ...
                'more positive per additional active IED.\n']);
        else
            fprintf(['conclusion: expected reward has a significantly ' ...
                'more negative per additional active IED.\n']);
        end
    else
        fprintf(['conclusion: no significant evidence that the ' ...
            'expected-reward slope changes with active IED count.\n']);
    end

    %% Return outputs with identical fields for IT, RT, and BR

    result = struct();
    result.code = config.code;
    result.displayName = config.displayName;
    result.postIEDWindowMilliseconds = postIEDWindowMilliseconds;
    result.countingProcessData = countingProcessData;
    result.trialSummary = trialSummary;
    result.coxResults = coxResults;
    result.beta = beta;
    result.logLikelihood = logLikelihood;
    result.baselineCumulativeHazard = baselineCumulativeHazard;
    result.stats = stats;
    result.clusterRobustCovariance = clusterRobustCovariance;
    result.clusterRobustSE = clusterRobustSE;
    result.clusterRobustZ = clusterRobustZ;
    result.clusterRobustP = clusterRobustP;
    result.predictorNames = predictorNames;
    result.patientLevels = patientLevels;
    result.referenceBalloonColor = referenceBalloonColor;
    result.nEndpointEvents = nEndpointEvents;
    result.nEventsOutsideWindow = nEventsOutsideWindow;
    result.nEventsInsideWindow = nEventsInsideWindow;
    result.timeOutsideWindowSeconds = timeOutsideWindowSeconds;
    result.timeInsideWindowSeconds = timeInsideWindowSeconds;
    result.totalIEDsRetained = totalIEDsRetained;
    result.modelEffectTable = modelEffectTable;
    result.expectedRewardSlopeOutsideIED = slopeOutsideIED;
    result.expectedRewardSlopeInsideIED = slopeInsideIED;

end

function modelFilePath = findModelFileForPatient( ...
    inputFolderName_modeling, modelFileList, patientID)

    modelFilePath = "";
    patientIDChar = char(patientID);
    escapedPatientID = regexptranslate('escape', patientIDChar);

    % The right-side boundary prevents patient 201902 from matching 201902r.
    patientPattern = [ ...
        '(^|[^A-Za-z0-9])', ...
        escapedPatientID, ...
        '([^A-Za-z0-9]|TDdataParamRecovery)'];

    matchedRows = false(length(modelFileList), 1);

    for ii = 1:length(modelFileList)
        matchedRows(ii) = ~isempty(regexpi( ...
            modelFileList(ii).name, patientPattern, 'once'));
    end

    matchedIndices = find(matchedRows);

    if isempty(matchedIndices)
        return;
    end

    if length(matchedIndices) > 1
        startsWithPatientID = false(length(matchedIndices), 1);
        for jj = 1:length(matchedIndices)
            startsWithPatientID(jj) = startsWith( ...
                string(modelFileList(matchedIndices(jj)).name), ...
                patientID, ...
                'IgnoreCase', true);
        end

        if sum(startsWithPatientID) == 1
            matchedIndices = matchedIndices(startsWithPatientID);
        else
            matchingNames = string({modelFileList(matchedIndices).name});
            [~, shortestRow] = min(strlength(matchingNames));
            matchedIndices = matchedIndices(shortestRow);
            fprintf(['Warning: multiple model files matched %s; ' ...
                'using %s.\n'], patientIDChar, ...
                modelFileList(matchedIndices).name);
        end
    end

    modelFilePath = string(fullfile( ...
        inputFolderName_modeling, ...
        modelFileList(matchedIndices).name));

end

function [expectedRewards, status] = readExpectedReward(modelData)

    expectedRewards = zeros(0, 1);
    status = "ok";

    if ~isfield(modelData, 'TDdataParamRecovery')
        status = "TDdataParamRecovery structure was not found";
        return;
    end

    TD = modelData.TDdataParamRecovery;

    requiredFields = {'bestApIdx', 'bestAnIdx', 'expectedReward'};

    for ff = 1:length(requiredFields)
        if ~isfield(TD, requiredFields{ff})
            status = "missing field " + string(requiredFields{ff});
            return;
        end
    end

    bestApIdx = double(TD.bestApIdx);
    bestAnIdx = double(TD.bestAnIdx);

    if ~isscalar(bestApIdx) || ~isscalar(bestAnIdx) || ...
            ~isfinite(bestApIdx) || ~isfinite(bestAnIdx)
        status = "bestApIdx or bestAnIdx is invalid";
        return;
    end

    bestApIdx = round(bestApIdx);
    bestAnIdx = round(bestAnIdx);

    expectedRewardArray = TD.expectedReward;

    if ndims(expectedRewardArray) < 3 || ...
            bestApIdx < 1 || ...
            bestAnIdx < 1 || ...
            bestApIdx > size(expectedRewardArray, 1) || ...
            bestAnIdx > size(expectedRewardArray, 2)
        status = "expectedReward array or best parameter indices are invalid";
        return;
    end

    expectedRewards = squeeze( ...
        expectedRewardArray(bestApIdx, bestAnIdx, :));
    expectedRewards = double(expectedRewards(:));

    if isempty(expectedRewards)
        status = "expectedReward vector is empty";
    end

end

