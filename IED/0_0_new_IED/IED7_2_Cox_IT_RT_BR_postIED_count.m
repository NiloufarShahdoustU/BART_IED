function plotForestPanel(ax, coxResults, config)

    hold(ax, 'on');

    nPredictors = height(coxResults);
    yPositions = (1:nPredictors)';

    hazardRatio = coxResults.hazardRatio;
    lowerError = hazardRatio - coxResults.hazardRatioCILow;
    upperError = coxResults.hazardRatioCIHigh - hazardRatio;

    errorbar( ...
        ax, ...
        hazardRatio, ...
        yPositions, ...
        lowerError, ...
        upperError, ...
        'horizontal', ...
        'o', ...
        'LineWidth', 1.2, ...
        'MarkerSize', 6, ...
        'MarkerFaceColor', config.color, ...
        'MarkerEdgeColor', config.color, ...
        'Color', config.color, ...
        'CapSize', 7);

    xline(ax, 1, '--', ...
        'Color', config.color, ...
        'LineWidth', 1);

    predictorLabels = [ ...
        "active IED count within " + ...
            string(config.postIEDWindowMilliseconds) + " ms"; ...
        "orange vs yellow"; ...
        "red vs yellow" ...
    ];

    set(ax, ...
        'YTick', yPositions, ...
        'YTickLabel', predictorLabels, ...
        'YDir', 'reverse', ...
        'XScale', 'log', ...
        'FontSize', 10, ...
        'FontName', 'Arial', ...
        'XColor', 'k', ...
        'YColor', 'k', ...
        'TickDir', 'out');

    xlabel(ax, 'hazard ratio with robust 95% CI', 'Color', 'k');
    title(ax, sprintf('%s: Cox model estimates', char(config.code)), ...
        'Color', 'k', 'FontWeight', 'bold');

    xLimits = calculatePositiveLogLimits( ...
        coxResults.hazardRatioCILow, ...
        coxResults.hazardRatioCIHigh);

    significantRows = ...
        isfinite(coxResults.pValue) & ...
        coxResults.pValue < 0.05 & ...
        isfinite(coxResults.hazardRatioCIHigh) & ...
        coxResults.hazardRatioCIHigh > 0;

    significanceX = NaN(nPredictors, 1);
    significanceX(significantRows) = ...
        1.10 .* coxResults.hazardRatioCIHigh(significantRows);

    if any(significantRows)
        xLimits(2) = max( ...
            xLimits(2), ...
            1.15 .* max(significanceX(significantRows)));
    end

    xlim(ax, xLimits);
    ylim(ax, [0.5, nPredictors + 0.5]);

    for ii = 1:nPredictors
        if significantRows(ii)
            text( ...
                ax, ...
                significanceX(ii), ...
                yPositions(ii), ...
                '*', ...
                'Color', 'k', ...
                'FontSize', 16, ...
                'FontWeight', 'bold', ...
                'HorizontalAlignment', 'left', ...
                'VerticalAlignment', 'middle');
        end
    end

    grid(ax, 'off');
    box(ax, 'off');
    hold(ax, 'off');

end

