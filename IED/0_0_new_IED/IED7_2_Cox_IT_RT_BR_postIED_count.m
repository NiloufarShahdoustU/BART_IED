function tickValues = chooseSparseObservedTicks( ...
    xValues, maximumNumberOfTicks)

    xValues = xValues(:);
    xValues = xValues(isfinite(xValues));
    xValues = unique(xValues, 'sorted');

    if isempty(xValues)
        tickValues = [0; 1];
        return;
    end

    if length(xValues) <= maximumNumberOfTicks
        tickValues = xValues;
        return;
    end

    targetValues = linspace( ...
        min(xValues), ...
        max(xValues), ...
        maximumNumberOfTicks);

    tickValues = NaN(maximumNumberOfTicks, 1);

    for tt = 1:maximumNumberOfTicks

        [~, closestRow] = min(abs(xValues - targetValues(tt)));
        tickValues(tt) = xValues(closestRow);

    end

    tickValues = unique( ...
        [xValues(1); tickValues; xValues(end)], ...
        'sorted');

end

function axisLimits = calculatePositiveLogLimits( ...
    lowerValues, ...
    upperValues)

    lowerValues = lowerValues(:);
    upperValues = upperValues(:);

    positiveLower = lowerValues( ...
        isfinite(lowerValues) & lowerValues > 0);

    positiveUpper = upperValues( ...
        isfinite(upperValues) & upperValues > 0);

    if isempty(positiveLower) || isempty(positiveUpper)
        axisLimits = [0.5 2];
        return;
    end

    minimumValue = min([positiveLower; positiveUpper; 1]);
    maximumValue = max([positiveLower; positiveUpper; 1]);

    logMinimum = log10(minimumValue);
    logMaximum = log10(maximumValue);

    if logMaximum == logMinimum

        logMinimum = logMinimum - 0.2;
        logMaximum = logMaximum + 0.2;

    else

        padding = 0.10 .* (logMaximum - logMinimum);
        logMinimum = logMinimum - padding;
        logMaximum = logMaximum + padding;

    end

    axisLimits = [10^logMinimum, 10^logMaximum];

end
