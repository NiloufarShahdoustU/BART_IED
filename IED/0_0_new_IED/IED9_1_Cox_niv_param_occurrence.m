function curveTable = createNivModerationCurve( ...
    beta, robustCovariance, observedNivRaw, ...
    nivSummary, numberOfCurvePoints)

    observedNivRaw = observedNivRaw(:);
    observedNivRaw = observedNivRaw(isfinite(observedNivRaw));

    if isempty(observedNivRaw)
        error('No observed participant Niv values were available.');
    end

    minimumNiv = min(observedNivRaw);
    maximumNiv = max(observedNivRaw);

    if minimumNiv == maximumNiv
        minimumNiv = minimumNiv - 0.01;
        maximumNiv = maximumNiv + 0.01;
    end

    nivAsymmetryRaw = linspace( ...
        minimumNiv, maximumNiv, numberOfCurvePoints)';

    nivAsymmetryZ = ...
        (nivAsymmetryRaw - nivSummary.mean) ./ nivSummary.sd;

    betaIED = beta(1);
    betaInteraction = beta(2);

    logHazardRatio = ...
        betaIED + betaInteraction .* nivAsymmetryZ;

    varianceLogHazardRatio = ...
        robustCovariance(1, 1) + ...
        (nivAsymmetryZ .^ 2) .* robustCovariance(2, 2) + ...
        2 .* nivAsymmetryZ .* robustCovariance(1, 2);

    varianceLogHazardRatio = max(varianceLogHazardRatio, 0);
    standardErrorLogHazardRatio = sqrt(varianceLogHazardRatio);

    logHazardRatioCILow = ...
        logHazardRatio - 1.96 .* standardErrorLogHazardRatio;

    logHazardRatioCIHigh = ...
        logHazardRatio + 1.96 .* standardErrorLogHazardRatio;

    hazardRatioPerAdditionalIED = exp(logHazardRatio);
    hazardRatioCILow = exp(logHazardRatioCILow);
    hazardRatioCIHigh = exp(logHazardRatioCIHigh);

    curveTable = table( ...
        nivAsymmetryRaw, ...
        nivAsymmetryZ, ...
        logHazardRatio, ...
        standardErrorLogHazardRatio, ...
        logHazardRatioCILow, ...
        logHazardRatioCIHigh, ...
        hazardRatioPerAdditionalIED, ...
        hazardRatioCILow, ...
        hazardRatioCIHigh, ...
        'VariableNames', { ...
            'nivAsymmetryRaw', ...
            'nivAsymmetryZ', ...
            'logHazardRatioPerAdditionalIED', ...
            'standardErrorLogHazardRatio', ...
            'logHazardRatioCILow', ...
            'logHazardRatioCIHigh', ...
            'hazardRatioPerAdditionalIED', ...
            'hazardRatioCILow', ...
            'hazardRatioCIHigh'});

end

