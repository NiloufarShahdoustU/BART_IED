        "Inflation time (IT)");

    ax2 = nexttile(layout, 2);
    plotNivModerationPanel(ax2, IT, colorIT, ...
        "Inflation time (IT)");

    ax3 = nexttile(layout, 3);
    plotOutcomeForestPanel(ax3, RT, colorRT, markerAlpha, ...
        "Response time (RT)");

    ax4 = nexttile(layout, 4);
