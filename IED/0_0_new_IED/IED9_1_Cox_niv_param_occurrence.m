function [nivTable, nivSummary] = ...
    readNivParameterTable(nivParameterFile)

    if ~exist(nivParameterFile, 'file')
        error('Niv parameter CSV was not found: %s', nivParameterFile);
    end

    % Read all columns as text so participant IDs containing suffixes such
    % as 202006u or 201902r are not converted to missing numeric values.
    fileID = fopen(nivParameterFile, 'r');

    if fileID == -1
        error('Could not open Niv parameter file: %s', nivParameterFile);
    end

    fileCleanup = onCleanup(@() fclose(fileID));
    headerLine = fgetl(fileID);

    if ~ischar(headerLine) || isempty(strtrim(headerLine))
        error('The Niv parameter file has no readable header row.');
    end

    % Support comma-, tab-, and semicolon-delimited files.
    if contains(headerLine, sprintf('	'))
        delimiter = sprintf('	');
    elseif contains(headerLine, ',')
        delimiter = ',';
    elseif contains(headerLine, ';')
        delimiter = ';';
    else
        error([ ...
            'Could not identify the delimiter in alpha_comparison.csv. ' ...
            'Expected commas, tabs, or semicolons.']);
    end

    headerCells = strsplit(headerLine, delimiter);
    headerNames = string(headerCells(:));

    normalizedHeaderNames = lower(strip(headerNames));
    normalizedHeaderNames = erase(normalizedHeaderNames, '"');
    normalizedHeaderNames = regexprep( ...
        normalizedHeaderNames, '[^a-z0-9]', '');

    idColumn = find(normalizedHeaderNames == "ptid", 1);
    alphaPlusColumn = find( ...
        normalizedHeaderNames == "fitalphaplus", 1);
    alphaMinusColumn = find( ...
        normalizedHeaderNames == "fitalphaminus", 1);

    if isempty(idColumn) || isempty(alphaPlusColumn) || ...
            isempty(alphaMinusColumn)
        error([ ...
            'alpha_comparison.csv must contain columns named: ' ...
            'ptID, fit_alpha_plus, and fit_alpha_minus.']);
    end

    numberOfColumns = length(headerCells);
    formatSpecification = repmat('%q', 1, numberOfColumns);

    rawColumns = textscan( ...
        fileID, ...
        formatSpecification, ...
        'Delimiter', delimiter, ...
        'ReturnOnError', false);

    clear fileCleanup;

    if isempty(rawColumns) || length(rawColumns) < numberOfColumns
        error('No readable data rows were found in: %s', nivParameterFile);
    end

    patientID = string(rawColumns{idColumn});
    alphaPlus = str2double(string(rawColumns{alphaPlusColumn}));
    alphaMinus = str2double(string(rawColumns{alphaMinusColumn}));

    patientID = patientID(:);
    alphaPlus = alphaPlus(:);
    alphaMinus = alphaMinus(:);

    patientID = strip(patientID);
    patientID = erase(patientID, '"');
    patientID = erase(patientID, char(39));

    denominator = alphaMinus + alphaPlus;
    nivAsymmetryRaw = NaN(size(denominator));

    validRows = ...
        ~ismissing(patientID) & ...
        strlength(strip(patientID)) > 0 & ...
        isfinite(alphaPlus) & ...
        isfinite(alphaMinus) & ...
        denominator > 0;

    nivAsymmetryRaw(validRows) = ...
        (alphaMinus(validRows) - alphaPlus(validRows)) ./ ...
        denominator(validRows);

    validRows = validRows & isfinite(nivAsymmetryRaw);

    patientID = strip(patientID(validRows));
    alphaPlus = alphaPlus(validRows);
    alphaMinus = alphaMinus(validRows);
    nivAsymmetryRaw = nivAsymmetryRaw(validRows);

    normalizedPatientID = normalizeParticipantID(patientID);

    nonemptyIDRows = strlength(normalizedPatientID) > 0;

    patientID = patientID(nonemptyIDRows);
    normalizedPatientID = normalizedPatientID(nonemptyIDRows);
    alphaPlus = alphaPlus(nonemptyIDRows);
    alphaMinus = alphaMinus(nonemptyIDRows);
    nivAsymmetryRaw = nivAsymmetryRaw(nonemptyIDRows);

    [~, uniqueRowIndices] = unique( ...
        normalizedPatientID, 'stable');

    if length(uniqueRowIndices) < length(normalizedPatientID)
        warning([ ...
            'Duplicate participant IDs were found in the Niv CSV. ' ...
            'Only the first row for each participant will be used.']);
    end

    patientID = patientID(uniqueRowIndices);
    normalizedPatientID = normalizedPatientID(uniqueRowIndices);
    alphaPlus = alphaPlus(uniqueRowIndices);
    alphaMinus = alphaMinus(uniqueRowIndices);
    nivAsymmetryRaw = nivAsymmetryRaw(uniqueRowIndices);

    nivMean = mean(nivAsymmetryRaw, 'omitnan');
    nivSD = std(nivAsymmetryRaw, 0, 'omitnan');

    if ~isfinite(nivSD) || nivSD <= eps
        error('Niv asymmetry has zero or invalid variance.');
    end

    nivAsymmetryZ = (nivAsymmetryRaw - nivMean) ./ nivSD;

    nivTable = table( ...
        patientID, ...
        normalizedPatientID, ...
        alphaPlus, ...
        alphaMinus, ...
        nivAsymmetryRaw, ...
        nivAsymmetryZ, ...
        'VariableNames', { ...
            'patientID', ...
            'normalizedPatientID', ...
            'alphaPlus', ...
            'alphaMinus', ...
            'nivAsymmetryRaw', ...
            'nivAsymmetryZ'});

    nivSummary = struct();
    nivSummary.mean = nivMean;
    nivSummary.sd = nivSD;
    nivSummary.nParticipants = height(nivTable);

    % Show suffix-bearing IDs immediately so their import can be verified.
    alphanumericRows = ~cellfun( ...
        @isempty, regexp(cellstr(patientID), '[a-zA-Z]', 'once'));

    if any(alphanumericRows)
        fprintf('\nAlphanumeric participant IDs preserved from Niv file:\n');
        disp(nivTable(alphanumericRows, { ...
            'patientID', ...
            'alphaPlus', ...
            'alphaMinus', ...
            'nivAsymmetryRaw'}));
    end

end

function values = convertTableColumnToDouble(rawValues)

    if isnumeric(rawValues) || islogical(rawValues)
        values = double(rawValues);
        return;
    end

    if isstring(rawValues)
        values = str2double(rawValues);
        return;
    end

    if ischar(rawValues)
        values = str2double(string(cellstr(rawValues)));
        return;
    end

    if iscell(rawValues)
        values = NaN(numel(rawValues), 1);

        for ii = 1:numel(rawValues)
            thisValue = rawValues{ii};

            if isnumeric(thisValue) && isscalar(thisValue)
                values(ii) = double(thisValue);
            else
                values(ii) = str2double(string(thisValue));
            end
        end

        return;
    end

    values = str2double(string(rawValues));

end

function [found, alphaPlus, alphaMinus, ...
    nivAsymmetryRaw, nivAsymmetryZ] = ...
    getNivParametersForPatient(nivTable, patientID)

    normalizedPatientID = normalizeParticipantID(patientID);

    matchingRows = ...
        nivTable.normalizedPatientID == normalizedPatientID;

    found = any(matchingRows);
    alphaPlus = NaN;
    alphaMinus = NaN;
    nivAsymmetryRaw = NaN;
    nivAsymmetryZ = NaN;

    if ~found
        return;
    end

    rowNumber = find(matchingRows, 1, 'first');

    alphaPlus = nivTable.alphaPlus(rowNumber);
    alphaMinus = nivTable.alphaMinus(rowNumber);
    nivAsymmetryRaw = nivTable.nivAsymmetryRaw(rowNumber);
    nivAsymmetryZ = nivTable.nivAsymmetryZ(rowNumber);

end

