function colorMap = createBlueWhiteRedColorMap(numberOfColors)

    if nargin < 1
        numberOfColors = 256;
    end

    numberOfColors = max(round(numberOfColors), 3);

    numberLower = floor(numberOfColors / 2);
    numberUpper = numberOfColors - numberLower;

    blueColor = [0.12 0.35 0.75];
    whiteColor = [0.97 0.97 0.97];
    redColor = [0.78 0.18 0.18];

    lowerHalf = [ ...
        linspace(blueColor(1), whiteColor(1), numberLower)', ...
        linspace(blueColor(2), whiteColor(2), numberLower)', ...
        linspace(blueColor(3), whiteColor(3), numberLower)'];

    upperHalf = [ ...
        linspace(whiteColor(1), redColor(1), numberUpper)', ...
        linspace(whiteColor(2), redColor(2), numberUpper)', ...
        linspace(whiteColor(3), redColor(3), numberUpper)'];

    colorMap = [lowerHalf; upperHalf];

end

function outputColor = mapValueToDivergingColor( ...
    value, colorLimits, colorMap)

    minimumValue = colorLimits(1);
    maximumValue = colorLimits(2);

    if maximumValue <= minimumValue
        outputColor = colorMap( ...
            round(size(colorMap, 1) / 2), :);
        return;
    end

    normalizedValue = ...
        (value - minimumValue) / ...
        (maximumValue - minimumValue);

    normalizedValue = min(max(normalizedValue, 0), 1);

    colorIndex = 1 + round( ...
        normalizedValue * (size(colorMap, 1) - 1));

    colorIndex = min(max(colorIndex, 1), ...
        size(colorMap, 1));

    outputColor = colorMap(colorIndex, :);

end

function trialRows = makePostIEDWindowRows( ...
    patientID, trialNumber, durationSeconds, IEDoccurrence, ...
    samplingFrequencyHz, balloonColorCode, finalEventObserved, ...
    postIEDWindowSeconds, alphaPlus, alphaMinus, ...
    nivAsymmetryRaw, nivAsymmetryZ)

    % IEDoccurrence columns:
    %   1 = trial number
    %   2 = channel
    %   3 = sample index from the beginning of RT or IT

    if isempty(IEDoccurrence) || size(IEDoccurrence, 2) < 3
        IEDtimes = [];
    else
        validIEDRows = ...
            isfinite(IEDoccurrence(:, 1)) & ...
            isfinite(IEDoccurrence(:, 3)) & ...
            round(IEDoccurrence(:, 1)) == trialNumber & ...
            IEDoccurrence(:, 3) >= 1;

        sampleIndices = double(IEDoccurrence(validIEDRows, 3));
        IEDtimes = sampleIndices ./ samplingFrequencyHz;

        IEDtimes = IEDtimes( ...
            isfinite(IEDtimes) & ...
            IEDtimes > 0 & ...
            IEDtimes < durationSeconds);

        % Keep every IED occurrence. Duplicate times are retained.
        IEDtimes = sort(IEDtimes);
    end

    if isempty(IEDtimes)
        breakTimes = [0; durationSeconds];
    else
        windowEndTimes = min( ...
            IEDtimes + postIEDWindowSeconds, durationSeconds);

        breakTimes = unique([ ...
            0; ...
            IEDtimes(:); ...
            windowEndTimes(:); ...
            durationSeconds], ...
            'sorted');
    end

    breakTimes = breakTimes( ...
        isfinite(breakTimes) & ...
        breakTimes >= 0 & ...
        breakTimes <= durationSeconds);

    breakTimes = unique(breakTimes, 'sorted');

    if length(breakTimes) < 2
        breakTimes = [0; durationSeconds];
    end

    tStart = breakTimes(1:end-1);
    tStop = breakTimes(2:end);

    validIntervals = tStop > tStart;
    tStart = tStart(validIntervals);
    tStop = tStop(validIntervals);

    intervalMidpoint = (tStart + tStop) ./ 2;

    postIED = zeros(length(tStart), 1);
    activeIEDWindowCount = zeros(length(tStart), 1);
    timeSinceMostRecentIED = NaN(length(tStart), 1);

    for kk = 1:length(tStart)

        if isempty(IEDtimes)
            continue;
        end

        activeWindows = ...
            intervalMidpoint(kk) >= IEDtimes & ...
            intervalMidpoint(kk) <= ...
                (IEDtimes + postIEDWindowSeconds);

        activeIEDWindowCount(kk) = sum(activeWindows);
        postIED(kk) = double(any(activeWindows));

        priorIEDtimes = IEDtimes(IEDtimes < intervalMidpoint(kk));

        if ~isempty(priorIEDtimes)
            timeSinceMostRecentIED(kk) = ...
                intervalMidpoint(kk) - max(priorIEDtimes);
        end

    end

    censored = true(length(tStart), 1);
    eventAtStop = false(length(tStart), 1);

    if finalEventObserved
        censored(end) = false;
        eventAtStop(end) = true;
    end

    trialRows = table( ...
        repmat(patientID, length(tStart), 1), ...
        repmat(trialNumber, length(tStart), 1), ...
        tStart, ...
        tStop, ...
        censored, ...
        eventAtStop, ...
        postIED, ...
        activeIEDWindowCount, ...
        timeSinceMostRecentIED, ...
        repmat(balloonColorCode, length(tStart), 1), ...
        repmat(samplingFrequencyHz, length(tStart), 1), ...
        repmat(alphaPlus, length(tStart), 1), ...
        repmat(alphaMinus, length(tStart), 1), ...
        repmat(nivAsymmetryRaw, length(tStart), 1), ...
        repmat(nivAsymmetryZ, length(tStart), 1), ...
        'VariableNames', { ...
            'patientID', ...
            'trialNumber', ...
            'tStart', ...
            'tStop', ...
            'censored', ...
            'eventAtStop', ...
            'postIED', ...
            'activeIEDWindowCount', ...
            'timeSinceMostRecentIED_seconds', ...
            'balloonColorCode', ...
            'samplingFrequencyHz', ...
            'alphaPlus', ...
            'alphaMinus', ...
            'nivAsymmetryRaw', ...
            'nivAsymmetryZ'});

end

function nIEDs = countValidIEDsInTrial( ...
    IEDoccurrence, trialNumber, durationSeconds, samplingFrequencyHz)

    nIEDs = 0;

    if isempty(IEDoccurrence) || size(IEDoccurrence, 2) < 3
        return;
    end

    validRows = ...
        isfinite(IEDoccurrence(:, 1)) & ...
        isfinite(IEDoccurrence(:, 3)) & ...
        round(IEDoccurrence(:, 1)) == trialNumber & ...
        IEDoccurrence(:, 3) >= 1;

    sampleIndices = double(IEDoccurrence(validRows, 3));
    IEDtimes = sampleIndices ./ samplingFrequencyHz;

    nIEDs = sum( ...
        isfinite(IEDtimes) & ...
        IEDtimes > 0 & ...
        IEDtimes < durationSeconds);

end

