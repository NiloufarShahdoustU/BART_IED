        "Banking (BR)");

    title(layout, ...
        'Whole-IED effects moderated by Niv learning asymmetry', ...
        'FontWeight', 'bold', ...
        'FontSize', 16);

end

function plotOutcomeForestPanel( ...
    ax, result, panelColor, markerAlpha, descriptiveTitle)

