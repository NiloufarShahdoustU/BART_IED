function result = runWholeIEDOutcomeAnalysis( ...
    inputFolderName_LFPIED, analysisType, settings, ...
    nivTable, nivSummary)

    [postIEDWindowMilliseconds, requiredFields] = ...
        getOutcomeSettings(analysisType, settings);

    postIEDWindowSeconds = postIEDWindowMilliseconds / 1000;

    fileList = dir(fullfile(inputFolderName_LFPIED, '*.LFPIED.mat'));

    if isempty(fileList)
        error('No .LFPIED.mat files were found in: %s', ...
            inputFolderName_LFPIED);
    end

    countingProcessData = emptyCountingProcessTable();
    trialSummary = emptyTrialSummaryTable();

    loadedParticipants = strings(0, 1);
    skippedNoNiv = strings(0, 1);
    skippedMissingData = strings(0, 1);

    fprintf('\n============================================================\n');
    fprintf('%s: building whole-IED counting-process data\n', analysisType);
    fprintf('Post-IED window: %d ms\n', postIEDWindowMilliseconds);
    fprintf('============================================================\n');

    for pt = 1:length(fileList)

        fileName = fileList(pt).name;
        fileNameParts = strsplit(fileName, '.');
        patientID = string(fileNameParts{1});

        fprintf('\nProcessing participant %s for %s\n', ...
            patientID, analysisType);

        [nivFound, alphaPlus, alphaMinus, ...
            nivAsymmetryRaw, nivAsymmetryZ] = ...
            getNivParametersForPatient(nivTable, patientID);

        if ~nivFound
            fprintf('Skipped: no matching valid Niv alpha parameters.\n');
            fprintf('Normalized LFPIED ID: %s\n', ...
                normalizeParticipantID(patientID));
            skippedNoNiv(end + 1, 1) = patientID;
            continue;
        end

        fprintf([ ...
            'Matched Niv values: alpha+ = %.4f, alpha- = %.4f, ' ...
            'raw Niv = %.4f, Niv z = %.4f\n'], ...
            alphaPlus, alphaMinus, nivAsymmetryRaw, nivAsymmetryZ);

        loadedData = load(fullfile(inputFolderName_LFPIED, fileName));

        if ~isfield(loadedData, 'LFPIED')
            fprintf('Skipped: LFPIED structure was not found.\n');
            skippedMissingData(end + 1, 1) = patientID;
            continue;
        end

        LFPIED = loadedData.LFPIED;

        missingField = false;

        for ff = 1:length(requiredFields)
            if ~isfield(LFPIED, requiredFields{ff})
                fprintf('Skipped: missing field %s.\n', requiredFields{ff});
                missingField = true;
            end
        end

        if missingField
            skippedMissingData(end + 1, 1) = patientID;
            continue;
        end

        RTs = double(LFPIED.RTs(:));
        isControl = double(LFPIED.isControl(:));
        balloonType = double(LFPIED.balloonType(:));
        balloonColorCode = mapBalloonColorCode(balloonType);

        switch analysisType
            case "RT"
                ITs = NaN(size(RTs));
                BankedTrials = NaN(size(RTs));
                vectorLengths = [ ...
                    length(RTs), ...
                    length(isControl), ...
                    length(balloonType)];
                IEDoccurrence = LFPIED.IED_occurance_RT;

            case "IT"
                ITs = double(LFPIED.ITs(:));
                BankedTrials = NaN(size(RTs));
                vectorLengths = [ ...
                    length(RTs), ...
                    length(ITs), ...
                    length(isControl), ...
                    length(balloonType)];
                IEDoccurrence = LFPIED.IED_occurance_IT;

            case "BR"
                ITs = double(LFPIED.ITs(:));
                BankedTrials = double(LFPIED.BankedTrials(:));
                vectorLengths = [ ...
                    length(RTs), ...
                    length(ITs), ...
                    length(BankedTrials), ...
                    length(isControl), ...
                    length(balloonType)];
                IEDoccurrence = LFPIED.IED_occurance_IT;

            otherwise
                error('Unknown analysis type: %s', analysisType);
        end

        if isfield(LFPIED, 'nTrials') && ...
                isscalar(LFPIED.nTrials) && ...
                isfinite(LFPIED.nTrials)
            vectorLengths(end + 1) = double(LFPIED.nTrials);
        end

        nTrials = floor(min(vectorLengths));

        if nTrials < 1
            fprintf('Skipped: no trials were available.\n');
            skippedMissingData(end + 1, 1) = patientID;
            continue;
        end

        RTs = RTs(1:nTrials);
        ITs = ITs(1:nTrials);
        BankedTrials = BankedTrials(1:nTrials);
        isControl = isControl(1:nTrials);
        balloonColorCode = balloonColorCode(1:nTrials);

        samplingFrequencyHz = getSamplingFrequency( ...
            LFPIED, settings.defaultSamplingFrequencyHz);

        participantTrialCountBefore = height(trialSummary);

        for trialNumber = 1:nTrials

            switch analysisType
                case "RT"
                    durationSeconds = RTs(trialNumber);
                    finalEventObserved = true;

                case "IT"
                    durationSeconds = ITs(trialNumber);
                    finalEventObserved = true;

                case "BR"
                    durationSeconds = ITs(trialNumber);
                    finalEventObserved = BankedTrials(trialNumber) == 1;
            end

            keepTrial = ...
                isControl(trialNumber) == 0 && ...
                isfinite(RTs(trialNumber)) && ...
                RTs(trialNumber) > 0 && ...
                RTs(trialNumber) <= settings.maximumRTSeconds && ...
                isfinite(durationSeconds) && ...
                durationSeconds > 0 && ...
                isfinite(balloonColorCode(trialNumber)) && ...
                ismember(balloonColorCode(trialNumber), [1 2 3]);

            if analysisType == "BR"
                keepTrial = keepTrial && ...
                    isfinite(BankedTrials(trialNumber)) && ...
                    ismember(BankedTrials(trialNumber), [0 1]);
            end

            if ~keepTrial
                continue;
            end

            trialRows = makePostIEDWindowRows( ...
                patientID, ...
                trialNumber, ...
                durationSeconds, ...
                IEDoccurrence, ...
                samplingFrequencyHz, ...
                balloonColorCode(trialNumber), ...
                finalEventObserved, ...
                postIEDWindowSeconds, ...
                alphaPlus, ...
                alphaMinus, ...
                nivAsymmetryRaw, ...
                nivAsymmetryZ);

            countingProcessData = [countingProcessData; trialRows]; 

            nIEDsInTrial = countValidIEDsInTrial( ...
                IEDoccurrence, trialNumber, durationSeconds, ...
                samplingFrequencyHz);

            intervalDuration = trialRows.tStop - trialRows.tStart;

            totalPostIEDExposureSeconds = sum( ...
                intervalDuration .* ...
                double(trialRows.postIED == 1), ...
                'omitnan');

            eventInPostIEDWindow = double( ...
                finalEventObserved && ...
                trialRows.postIED(end) == 1);

            newTrialSummaryRow = table( ...
                patientID, ...
                trialNumber, ...
                durationSeconds, ...
                double(finalEventObserved), ...
                eventInPostIEDWindow, ...
                nIEDsInTrial, ...
                totalPostIEDExposureSeconds, ...
                balloonColorCode(trialNumber), ...
                samplingFrequencyHz, ...
                alphaPlus, ...
                alphaMinus, ...
                nivAsymmetryRaw, ...
                nivAsymmetryZ, ...
                'VariableNames', trialSummary.Properties.VariableNames);

            trialSummary = [trialSummary; newTrialSummaryRow]; 

        end

        if height(trialSummary) > participantTrialCountBefore
            loadedParticipants(end + 1, 1) = patientID;
        else
            fprintf('No valid trials remained for this participant.\n');
        end

    end

    if isempty(countingProcessData)
        error('No valid counting-process rows were created for %s.', ...
            analysisType);
    end

    %% Build predictor matrix

    [countingProcessData.patientStratum, patientLevels] = ...
        findgroups(countingProcessData.patientID);

    iedOccurrence = countingProcessData.postIED;
    iedByNiv = iedOccurrence .* countingProcessData.nivAsymmetryZ;

    X = [iedOccurrence, iedByNiv];

    predictorNames = [ ...
        "any_ied_occurrence"; ...
        "any_ied_occurrence_x_niv_z"];

    predictorDisplayLabels = [ ...
        "Any IED occurrence"; ...
        "IED occurrence x Niv (1 SD)"];

    observedColors = unique( ...
        countingProcessData.balloonColorCode, 'sorted');
    observedColors = observedColors(ismember(observedColors, [1 2 3]));

    if isempty(observedColors)
        error('No valid balloon colors were found for %s.', analysisType);
    end

    if ismember(1, observedColors)
        referenceColor = 1;
    else
        referenceColor = observedColors(1);
    end

    colorNames = ["yellow", "orange", "red"];
    comparisonColors = observedColors(observedColors ~= referenceColor);

    for cc = 1:length(comparisonColors)
        colorCode = comparisonColors(cc);
        X(:, end + 1) = double( ...
            countingProcessData.balloonColorCode == colorCode); 

        predictorNames(end + 1, 1) = ...
            colorNames(colorCode) + "_vs_" + colorNames(referenceColor); 

        predictorDisplayLabels(end + 1, 1) = ...
            capitalizeFirst(colorNames(colorCode)) + ...
            " vs " + capitalizeFirst(colorNames(referenceColor)); 
    end

    T = [countingProcessData.tStart, countingProcessData.tStop];
    censoring = logical(countingProcessData.censored);
    strata = countingProcessData.patientStratum;

    validRows = ...
        all(isfinite(X), 2) & ...
        all(isfinite(T), 2) & ...
        T(:, 1) >= 0 & ...
        T(:, 2) > T(:, 1) & ...
        isfinite(strata);

    X = X(validRows, :);
    T = T(validRows, :);
    censoring = censoring(validRows);
    strata = strata(validRows);
    countingProcessData = countingProcessData(validRows, :);

    %% Model diagnostics before fitting

    nModelParticipants = length(unique(countingProcessData.patientID));

    participantsWithIED = unique( ...
        countingProcessData.patientID( ...
        countingProcessData.activeIEDWindowCount > 0));

    nParticipantsWithIED = length(participantsWithIED);

    nEndpointEvents = sum(countingProcessData.eventAtStop == 1);

    nEventsOutsideWindow = sum( ...
        countingProcessData.eventAtStop == 1 & ...
        countingProcessData.postIED == 0);

    nEventsInsideWindow = sum( ...
        countingProcessData.eventAtStop == 1 & ...
        countingProcessData.postIED == 1);

    intervalDuration = ...
        countingProcessData.tStop - countingProcessData.tStart;

    timeOutsideWindowSeconds = sum( ...
        intervalDuration .* ...
        double(countingProcessData.postIED == 0), ...
        'omitnan');

    timeInsideWindowSeconds = sum( ...
        intervalDuration .* ...
        double(countingProcessData.postIED == 1), ...
        'omitnan');

    if nParticipantsWithIED < settings.minimumParticipantsWithIED
        error([ ...
            '%s has only %d participants with IED exposure. ' ...
            'At least %d are required for the IED x Niv interaction.'], ...
            analysisType, nParticipantsWithIED, ...
            settings.minimumParticipantsWithIED);
    end

    if timeInsideWindowSeconds <= 0
        error('No post-IED exposure time was available for %s.', ...
            analysisType);
    end

    if nEventsInsideWindow < ...
            settings.minimumEndpointEventsInsideWindow
        warning('%s has no endpoint events inside post-IED windows.', ...
            analysisType);
    end

    if nEventsOutsideWindow < ...
            settings.minimumEndpointEventsOutsideWindow
        warning('%s has no endpoint events outside post-IED windows.', ...
            analysisType);
    end

    if size(X, 1) <= size(X, 2)
        error('Insufficient rows for the %s predictor matrix.', ...
            analysisType);
    end

    if std(X(:, 1), 0, 'omitnan') <= eps
        error('The IED occurrence indicator has no variation for %s.', ...
            analysisType);
    end

    if std(X(:, 2), 0, 'omitnan') <= eps
        error('The IED x Niv interaction has no variation for %s.', ...
            analysisType);
    end

    %% Fit participant-stratified Cox model

    coxOptions = statset('coxphfit');
    coxOptions.Display = 'final';
    coxOptions.MaxIter = 1000;
    coxOptions.MaxFunEvals = 5000;

    [beta, logLikelihood, baselineCumulativeHazard, stats] = ...
        coxphfit( ...
            X, ...
            T, ...
            'Censoring', censoring, ...
            'Strata', strata, ...
            'Ties', 'efron', ...
            'Baseline', 0, ...
            'Options', coxOptions);

    %% Participant-clustered robust inference

    [clusterRobustCovariance, clusterRobustSE, ...
        clusterRobustZ, clusterRobustP] = ...
        computeClusterRobustInference( ...
            stats, beta, countingProcessData.patientStratum);

    %% Results table

    ciLowBeta = beta - 1.96 .* clusterRobustSE;
    ciHighBeta = beta + 1.96 .* clusterRobustSE;

    hazardRatio = exp(beta);
    hazardRatioCILow = exp(ciLowBeta);
    hazardRatioCIHigh = exp(ciHighBeta);

    interpretation = strings(length(beta), 1);

    interpretation(1) = getIEDMainEffectInterpretation(analysisType);
    interpretation(2) = ...
        "Multiplicative change in the IED hazard ratio per 1-SD increase in Niv asymmetry.";

    if length(beta) > 2
        interpretation(3:end) = ...
            "Balloon-color effect relative to the reference color.";
    end

    coxResults = table( ...
        predictorNames(:), ...
        predictorDisplayLabels(:), ...
        beta(:), ...
        stats.se(:), ...
        stats.z(:), ...
        stats.p(:), ...
        clusterRobustSE(:), ...
        clusterRobustZ(:), ...
        clusterRobustP(:), ...
        ciLowBeta(:), ...
        ciHighBeta(:), ...
        hazardRatio(:), ...
        hazardRatioCILow(:), ...
        hazardRatioCIHigh(:), ...
        interpretation(:), ...
        'VariableNames', { ...
            'predictor', ...
            'displayLabel', ...
            'beta_logHazard', ...
            'modelBasedSE', ...
            'modelBasedZ', ...
            'modelBasedPValue', ...
            'clusterRobustSE', ...
            'clusterRobustZ', ...
            'pValue', ...
            'betaCILow', ...
            'betaCIHigh', ...
            'hazardRatio', ...
            'hazardRatioCILow', ...
            'hazardRatioCIHigh', ...
            'interpretation'});

    %% Create Niv moderation curve

    observedPatientRows = unique( ...
        countingProcessData(:, { ...
            'patientID', 'nivAsymmetryRaw', 'nivAsymmetryZ'}), ...
        'rows', 'stable');

    nivCurveTable = createNivModerationCurve( ...
        beta, clusterRobustCovariance, ...
        observedPatientRows.nivAsymmetryRaw, ...
        nivSummary, settings.numberOfNivCurvePoints);

    %% Print outcome summary

    fprintf('\n============================================================\n');
    fprintf('%s whole-IED Cox model with Niv moderation\n', analysisType);
    fprintf('============================================================\n');
    fprintf('Post-IED window: %d ms\n', postIEDWindowMilliseconds);
    fprintf('Participants in model: %d\n', nModelParticipants);
    fprintf('Participants with IED exposure: %d\n', ...
        nParticipantsWithIED);
    fprintf('Valid trials: %d\n', height(trialSummary));
    fprintf('Counting-process rows: %d\n', ...
        height(countingProcessData));
    fprintf('Endpoint events: %d\n', nEndpointEvents);
    fprintf('Events outside window: %d\n', ...
        nEventsOutsideWindow);
    fprintf('Events inside window: %d\n', ...
        nEventsInsideWindow);
    fprintf('At-risk time outside window: %.3f s\n', ...
        timeOutsideWindowSeconds);
    fprintf('At-risk time inside window: %.3f s\n', ...
        timeInsideWindowSeconds);
    fprintf('Log likelihood: %.6f\n\n', logLikelihood);

    disp(coxResults);

    mainIEDRow = coxResults(1, :);
    interactionRow = coxResults(2, :);

    fprintf('\nIED effect at mean Niv asymmetry:\n');
    fprintf('beta = %.6f\n', mainIEDRow.beta_logHazard);
    fprintf('HR for any IED occurrence versus none = %.6f\n', ...
        mainIEDRow.hazardRatio);
    fprintf('95%% CI = [%.6f, %.6f]\n', ...
        mainIEDRow.hazardRatioCILow, ...
        mainIEDRow.hazardRatioCIHigh);
    fprintf('p = %.6g\n', mainIEDRow.pValue);

    fprintf('\nIED x Niv interaction:\n');
    fprintf('beta = %.6f\n', interactionRow.beta_logHazard);
    fprintf('HR multiplier per 1-SD increase in Niv = %.6f\n', ...
        interactionRow.hazardRatio);
    fprintf('95%% CI = [%.6f, %.6f]\n', ...
        interactionRow.hazardRatioCILow, ...
        interactionRow.hazardRatioCIHigh);
    fprintf('p = %.6g\n', interactionRow.pValue);

    if interactionRow.pValue < 0.05
        if interactionRow.beta_logHazard > 0
            fprintf([ ...
                'Conclusion: the IED hazard effect becomes more positive ' ...
                'as Niv asymmetry increases.\n']);
        else
            fprintf([ ...
                'Conclusion: the IED hazard effect becomes more negative ' ...
                'as Niv asymmetry increases.\n']);
        end
    else
        fprintf([ ...
            'Conclusion: Niv asymmetry does not significantly moderate ' ...
            'the IED hazard effect.\n']);
    end

    %% Return all outputs

    result = struct();
    result.analysisType = analysisType;
    result.postIEDWindowMilliseconds = postIEDWindowMilliseconds;
    result.postIEDWindowSeconds = postIEDWindowSeconds;
    result.countingProcessData = countingProcessData;
    result.trialSummary = trialSummary;
    result.coxResults = coxResults;
    result.nivCurveTable = nivCurveTable;
    result.observedPatientNiv = observedPatientRows;
    result.beta = beta;
    result.logLikelihood = logLikelihood;
    result.baselineCumulativeHazard = baselineCumulativeHazard;
    result.stats = stats;
    result.clusterRobustCovariance = clusterRobustCovariance;
    result.clusterRobustSE = clusterRobustSE;
    result.clusterRobustZ = clusterRobustZ;
    result.clusterRobustP = clusterRobustP;
    result.predictorNames = predictorNames;
    result.predictorDisplayLabels = predictorDisplayLabels;
    result.patientLevels = patientLevels;
    result.loadedParticipants = unique(loadedParticipants, 'stable');
    result.skippedNoNiv = unique(skippedNoNiv, 'stable');
    result.skippedMissingData = unique(skippedMissingData, 'stable');
    result.referenceBalloonColor = colorNames(referenceColor);
    result.nModelParticipants = nModelParticipants;
    result.nParticipantsWithIED = nParticipantsWithIED;
    result.nEndpointEvents = nEndpointEvents;
    result.nEventsOutsideWindow = nEventsOutsideWindow;
    result.nEventsInsideWindow = nEventsInsideWindow;
    result.timeOutsideWindowSeconds = timeOutsideWindowSeconds;
    result.timeInsideWindowSeconds = timeInsideWindowSeconds;

end

function [postIEDWindowMilliseconds, requiredFields] = ...
    getOutcomeSettings(analysisType, settings)

    switch analysisType
        case "RT"
            postIEDWindowMilliseconds = ...
                settings.postIEDWindowMillisecondsRT;
            requiredFields = { ...
                'RTs', 'isControl', 'balloonType', ...
                'IED_occurance_RT'};

        case "IT"
            postIEDWindowMilliseconds = ...
                settings.postIEDWindowMillisecondsIT;
            requiredFields = { ...
                'RTs', 'ITs', 'isControl', 'balloonType', ...
                'IED_occurance_IT'};

        case "BR"
            postIEDWindowMilliseconds = ...
                settings.postIEDWindowMillisecondsBR;
            requiredFields = { ...
                'RTs', 'ITs', 'BankedTrials', ...
                'isControl', 'balloonType', ...
                'IED_occurance_IT'};

        otherwise
            error('Unknown analysis type: %s', analysisType);
    end

end

function saveOutcomeOutputs(result, outputFolderName)

    prefix = char(result.analysisType);

    countingProcessOutputFile = fullfile( ...
        outputFolderName, ...
        sprintf('%s_whole_IED_x_Niv_counting_process_data.csv', prefix));

    trialSummaryOutputFile = fullfile( ...
        outputFolderName, ...
        sprintf('%s_whole_IED_x_Niv_trial_summary.csv', prefix));

    coxResultsOutputFile = fullfile( ...
        outputFolderName, ...
        sprintf('%s_whole_IED_x_Niv_cox_results.csv', prefix));

    nivCurveOutputFile = fullfile( ...
        outputFolderName, ...
        sprintf('%s_whole_IED_x_Niv_moderation_curve.csv', prefix));

    patientNivOutputFile = fullfile( ...
        outputFolderName, ...
        sprintf('%s_whole_IED_x_Niv_patient_values.csv', prefix));

    baselineHazardOutputFile = fullfile( ...
        outputFolderName, ...
        sprintf('%s_whole_IED_x_Niv_baseline_cumulative_hazard.csv', prefix));

    modelOutputFile = fullfile( ...
        outputFolderName, ...
        sprintf('%s_whole_IED_x_Niv_model.mat', prefix));

    writetable(result.countingProcessData, countingProcessOutputFile);
    writetable(result.trialSummary, trialSummaryOutputFile);
    writetable(result.coxResults, coxResultsOutputFile);
    writetable(result.nivCurveTable, nivCurveOutputFile);
    writetable(result.observedPatientNiv, patientNivOutputFile);
    writematrix(result.baselineCumulativeHazard, ...
        baselineHazardOutputFile);

    outcomeResult = result; 
    save(modelOutputFile, 'outcomeResult');

    fprintf('\nSaved %s outputs:\n', prefix);
    fprintf('%s\n', countingProcessOutputFile);
    fprintf('%s\n', trialSummaryOutputFile);
    fprintf('%s\n', coxResultsOutputFile);
    fprintf('%s\n', nivCurveOutputFile);
    fprintf('%s\n', patientNivOutputFile);
    fprintf('%s\n', baselineHazardOutputFile);
    fprintf('%s\n', modelOutputFile);

end

function fig = plotCombinedThreeByTwoFigure( ...
    IT, RT, BR, colorIT, colorRT, colorBR, markerAlpha)

