    nPredictors = height(data);
    yPositions = (1:nPredictors)';

    hold(ax, 'on');

    xline(ax, 1, '--', ...
        'Color', [0.35 0.35 0.35], ...
        'LineWidth', 1.1);

    for ii = 1:nPredictors
        plot(ax, ...
            [data.hazardRatioCILow(ii), ...
             data.hazardRatioCIHigh(ii)], ...
            [yPositions(ii), yPositions(ii)], ...
            '-', ...
            'Color', panelColor, ...
            'LineWidth', 1.6);
    end

    scatter(ax, ...
        data.hazardRatio, ...
        yPositions, ...
        64, ...
        panelColor, ...
        'filled', ...
        'MarkerFaceAlpha', markerAlpha, ...
        'MarkerEdgeColor', 'none');

    ax.XScale = 'log';
    ax.YDir = 'reverse';
    ax.YTick = yPositions;
    ax.YTickLabel = data.displayLabel;
    ax.TickLabelInterpreter = 'none';
    ax.TickDir = 'out';
    ax.FontName = 'Arial';
    ax.FontSize = 10;
    ax.XColor = 'k';
    ax.YColor = 'k';

    xLimits = calculatePositiveLogLimits( ...
        data.hazardRatioCILow, data.hazardRatioCIHigh);
    xlim(ax, xLimits);
    ylim(ax, [0.5, nPredictors + 0.5]);

    xlabel(ax, 'Hazard ratio with robust 95% CI');
    title(ax, {char(descriptiveTitle), ...
        sprintf('%d-ms post-IED Cox estimates', ...
        result.postIEDWindowMilliseconds)}, ...
        'FontWeight', 'bold');

    grid(ax, 'off');
    box(ax, 'off');
    hold(ax, 'off');

