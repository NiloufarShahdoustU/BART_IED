% Predictor 1 is the number of individual IED windows active at each time.
% Each IED contributes separately, including multiple IEDs in one channel.
% Yellow is the reference balloon color.
X = countingProcessData.activeIEDWindowCount;
predictorNames = "active_ied_count_within_500ms";

X = [X, double(countingProcessData.balloonColorCode == 2)];
predictorNames(end + 1, 1) = "orange_vs_yellow";

X = [X, double(countingProcessData.balloonColorCode == 3)];
predictorNames(end + 1, 1) = "red_vs_yellow";

